# Aumentar a porcentagem em 10%
def aumentar(n):
    porcentagem = (n * 10) / 100
    aumento = porcentagem + n
    return aumento

# Diminuir a porcentagem em 10%
def diminuir(n):
    porcentagem = (n * 10) / 100
    dimnuir = n - porcentagem
    return dimnuir

# Dobro do valor
def dobro(n):
    valor = 2 * n
    return valor

# Metade do valor
def metade(n):
    valor = n / 2
    return valor